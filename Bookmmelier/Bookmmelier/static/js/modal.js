function popOpen() {

    var modalPop = document.querySelector('.modal-wrap');
    var modalBg = document.querySelector('.modal-bg');

    modalPop.style.display = 'block';
    modalBg.style.display = 'block';

}

function popClose() {
    var modalPop = document.querySelector('.modal-wrap');
    var modalBg = document.querySelector('.modal-bg');

    modalPop.style.display = 'none';
    modalBg.style.display = 'none';

}